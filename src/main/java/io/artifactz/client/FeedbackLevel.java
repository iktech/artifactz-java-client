package io.artifactz.client;

/**
 * This enum describes allowed levels of the feedback messages used by the Feedback interface
 *
 * @see Feedback
 */
public enum FeedbackLevel {
    /**
     * Debug messages
     */
    DEBUG,
    /**
     * Information message
     */
    INFO,

    /**
     * Warning messages
     */
    WARNING,

    /**
     * Error message
     */
    ERROR;
}
